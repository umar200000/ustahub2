extension SvgExt on String {
  SvgPicture svg({
    Color? color,
    double? width,
    double? height,
    BoxFit? boxFit,
  }) {
    return SvgPicture.asset(
      this,
      colorFilter: color != null
          ? ColorFilter.mode(color, BlendMode.srcIn)
          : null,
      width: width,
      height: height,
      fit: boxFit ?? BoxFit.cover,
    );
  }

  SvgPicture svgNetwork({
    Color? color,
    double? width,
    double? height,
    BoxFit? boxFit,
  }) {
    return SvgPicture.network(
      this,
      colorFilter: color != null
          ? ColorFilter.mode(color, BlendMode.srcIn)
          : null,
      width: width,
      height: height,
      fit: boxFit ?? BoxFit.cover,
    );
  }
